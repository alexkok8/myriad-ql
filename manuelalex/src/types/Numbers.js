export class Numbers {
    constructor(value, location){
        this.value = value;
        this.location = location;
    }

    getValue(){
        return this.value;
    }

    getLocation(){
        return this.location;
    }

    evaluate(visitor){
        return visitor.evaluateNumbers(this);
    }



}