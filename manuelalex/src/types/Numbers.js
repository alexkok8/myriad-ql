export class Numbers {
    constructor(value, location) {
        this.value = value;
        this.location = location;
    }

    getValue() {
        return this.value;
    }

    getLocation() {
        return this.location;
    }

    accept(visitor, ...params) {
        return visitor.visitNumbers(this, ...params);
    }

}